dic = {
    "마이크로소프트": "www.microsoft.com",
    "파이썬": "www.python.org",
    "애플": "www.apple.com"
}

print("dic.items(): ", dic.items(), end="\n")
print("dic.keys(): ", dic.keys(), end="\n")
print("dic.values(): ", dic.values(), end="\n")
print("dic['파이썬']: ", dic['파이썬'], end="\n")
print("dic.get('파이썬'): ", dic.get('파이썬'), end="\n")
print("type(dic): ", type(dic), end="\n")
